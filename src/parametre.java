public class parametre {
    private String name;
    private int value;

    public void parametre(String name, int value){
       this.name=name;
       this.value=value;
    }

    public void setValue(int V){
        this.value = V;
    }

    public String getName() {
        return name;
    }
    public int getValue() {
        return value;
    }
}
